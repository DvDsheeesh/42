void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	mid;

	i = 0;
	while (i < size / 2)
	{
		mid = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - i - 1] = mid;
		i++;
	}
}
