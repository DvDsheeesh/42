#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((s1[i] || s2[i]) && i < n)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int	main(void)
{
	char	*s1 = "hi bro1";
	char	*s2 = "hi bro5";
	printf("%d", ft_strncmp(s1, s2, 6));
	return (0);
}
