#include "libft.h"

size_t	ft_count_word(char const *s, char c)
{
	size_t	cc;
	size_t	i;

	cc = 0;
	i = 0;
	while (s[i])
	{
		while (s[i] == c && s[i])
			i++;
		if (s[i])
			cc++;
		while (s[i] != c && s[i])
			i++;
	}
	return (cc);
}
